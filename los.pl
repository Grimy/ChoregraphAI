#!/usr/bin/perl

use strict;
use List::MoreUtils qw(uniq);
$" = ' || '; #"
$\ = $/;

sub id($$) { my ($x, $y) = @_; $y * ($y + 1) / 2 + $x - 1 - ($x == 10)}

sub los($$) {
	my ($x, $y) = @_;
	my $cx = int($x + .5);
	my $cy = int($y + .5);
	my $path = 0;
	return ~0 if $cx > $cy;
	while ($cy) {
		my $err_x = abs(($cx - 1 - $x) * $y - ($cy - $y) * $x);
		my $err_y = abs(($cx - $x) * $y - ($cy - 1 - $y) * $x);
		$path |= 1 << id($cx, $cy) if ($cx < 10 && $cy <= 10);
		$path |= 1 << id($cx - 1, $cy) if ($cx > 0 && abs($err_x - $err_y) < .001);
		--(!$cx || $err_y < $err_x + .001 ? $cy : $cx);
	}
	return $path;
}

print <<'END';
// los.c - code generated by los.pl - DO NOT EDIT!
#include "chore.h"
void cast_light(Tile *tile, i64 x, i64 y) {
Tile *row = tile;
u64 walls = 0;
END

for my $y (1..10) {
	print "tile = (row += y);\n";
	for my $x (0..$y) {
		my @masks = uniq map {$_ & (1 << id($x, $y)) - 1} (
			los($x - .51, $y - .51),
			los($x + .51, $y + .51),
			los($x - .51, $y + .51),
			los($x + .51, $y - .51),
			los($x, $y),
		);
		for my $mask (@masks) {
			@masks = grep {$_ == $mask || $mask &~ $_} @masks;
		}
		@masks = map {sprintf "!(walls & $_)"} grep {~$_} @masks;

		print "if (@masks) " x (id($x, $y) >= 4), "tile->revealed = true;\n";
		printf "if (tile->hp == 5) { walls |= %#x; goto label$y; }\n",
			(1 << id($y + 1, $y)) - (1 << id($x, $y));
		print("label10: (void) 0;\n}\n"), exit if $x == 10;
		printf "walls |= (u64) (tile->class == WALL) << %d;\n", id($x, $y);
		print "tile += x;\n";
	}
	print "label$y:";
	printf "if (walls >= %#x) return;\n", (1 << id($y + 1, $y)) - (1 << id(0, $y));
	printf "if (!(walls & %#x))\ntile->revealed = true;\n", los($y + 1 - .51, $y + .51);
}
